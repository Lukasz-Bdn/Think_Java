public class Zool {

	public static void zool(int x, String s1, String s2) {
		System.out.print(s1);
		System.out.print(x);
		System.out.println(s2);
	}

	public static void main(String[] args) {
		String s8 = "Moja liczba to ";
		String s9 = " to jest fajna liczba";
		int y=5;
		
		zool(y, s8, s9);
	}

}
